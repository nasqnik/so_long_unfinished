#include "libft.h"

long	ft_atoi(const char *str)
{
	int					sign;
	unsigned long long	result;

	sign = 1;
	result = 0;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + *str - '0';
		if (sign == -1 && result >= LLONG_MAX)
			return (2147483649);
		if (result >= LLONG_MAX)
			return (2147483649);
		str++;
	}
	return (result * sign);
}
